	enum class TriangleCullMode
	{
		FrontFaceCulling = -1,
		BackFaceCulling = 1,
		NoCulling = 0
	};

	struct Triangle
	{
		Triangle() = default;
		Triangle(const Vector3& _v0, const Vector3& _v1, const Vector3& _v2, const Vector3& _normal):
			v0{_v0}, v1{_v1}, v2{_v2}, normal{_normal.Normalized()}{}

		Triangle(const Vector3& _v0, const Vector3& _v1, const Vector3& _v2) :
			v0{ _v0 }, v1{ _v1 }, v2{ _v2 }
		{
			const Vector3 edgeV0V1 = v1 - v0;
			const Vector3 edgeV0V2 = v2 - v0;
			normal = Vector3::Cross(edgeV0V1, edgeV0V2).Normalized();
		}

		Vector3 v0{};
		Vector3 v1{};
		Vector3 v2{};

		Vector3 normal{};

		TriangleCullMode cullMode{};
		unsigned char materialIndex{};
	};

	struct TriangleMesh
	{
		TriangleMesh() = default;
		TriangleMesh(const std::vector<Vector3>& _positions, const std::vector<int>& _indices, TriangleCullMode _cullMode):
		positions(_positions), indices(_indices), cullMode(_cullMode)
		{
			//Calculate Normals
			CalculateNormals();

			//Update Transforms
			UpdateTransforms();
		}

		TriangleMesh(const std::vector<Vector3>& _positions, const std::vector<int>& _indices, const std::vector<Vector3>& _normals, TriangleCullMode _cullMode) :
			positions(_positions), indices(_indices), normals(_normals), cullMode(_cullMode)
		{
			UpdateTransforms();
		}

		std::vector<Vector3> positions{};
		std::vector<Vector3> normals{};
		std::vector<int> indices{};
		unsigned char materialIndex{};

		TriangleCullMode cullMode{TriangleCullMode::BackFaceCulling};

		Matrix rotationTransform{};
		Matrix translationTransform{};
		Matrix scaleTransform{};

		std::vector<Vector3> transformedPositions{};
		std::vector<Vector3> transformedNormals{};

		void Translate(const Vector3& translation)
		{
			translationTransform = Matrix::CreateTranslation(translation);
		}

		void RotateY(float yaw)
		{
			rotationTransform = Matrix::CreateRotationY(yaw);
		}

		void Scale(const Vector3& scale)
		{
			scaleTransform = Matrix::CreateScale(scale);
		}

		void AppendTriangle(const Triangle& triangle, bool ignoreTransformUpdate = false)
		{
			int startIndex = static_cast<int>(positions.size());

			positions.push_back(triangle.v0);
			positions.push_back(triangle.v1);
			positions.push_back(triangle.v2);

			indices.push_back(startIndex);
			indices.push_back(++startIndex);
			indices.push_back(++startIndex);

			normals.push_back(triangle.normal);

			//Not ideal, but making sure all vertices are updated
			if(!ignoreTransformUpdate)
				UpdateTransforms();
		}

		void CalculateNormals()
		{
			//Clear all existing normals
			normals.clear();

			//Reserve capacity equal to amount of triangles
			normals.reserve(int(indices.size() / 3));

			//Loop over all indices in sets of 3 (each triangle has 3 points)
			for (size_t index{}; index < indices.size() - 2; index += 3)
			{
				size_t i0 = indices[index];
				size_t i1 = indices[index + 1];
				size_t i2 = indices[index + 2];

				Vector3 edgeV0V1 = positions[i1] - positions[i0];
				Vector3 edgeV0V2 = positions[i2] - positions[i0];

				normals.emplace_back(Vector3::Cross(edgeV0V1, edgeV0V2).Normalized());
			}
		}

		void UpdateTransforms()
		{
			//Clear all existing transformed
			transformedPositions.clear();
			transformedNormals.clear();

			//Reserve capacity equal to amount of triangles
			transformedPositions.reserve(positions.size());
			transformedNormals.reserve(normals.size());

			//Resize equal to amount
			//transformedPositions.resize(positions.size());
			//transformedNormals.resize(normals.size());

			//Calculate Final Transform 
			const Matrix finalTransform = scaleTransform * rotationTransform * translationTransform;

			//Transform Positions (positions > transformedPositions)
			//for (size_t i{}; i < positions.size(); ++i)
			//{
			//	transformedPositions[i] = std::move(finalTransform.TransformPoint(positions[i]));
			//}
			for (const Vector3& pos : positions)
			{
				transformedPositions.emplace_back(finalTransform.TransformPoint(pos));
			}

			//Transform Normals (normals > transformedNormals)
			//for (size_t i{}; i < normals.size(); ++i)
			//{
			//	transformedNormals[i] = std::move(finalTransform.TransformVector(normals[i]));
			//}
			for (const Vector3& n : normals)
			{
				transformedNormals.emplace_back(finalTransform.TransformVector(n));
			}
		}
	};
